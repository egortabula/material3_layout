/// Enumeration of the different types of navigation for the `NavigationScaffold` widget.
///
/// The options are:
///
/// * `railAndBottomNavBar`: Shows a navigation rail on medium and extended screens and a bottom navigation bar on the ocompact screen.
///
/// * `modalDrawer`: Shows a material 3 modal drawer.
///
/// * `drawer`: Shows a material 3 standard drawer.
enum NavigationTypeEnum {
  railAndBottomNavBar,
  modalDrawer,
  drawer,
}

